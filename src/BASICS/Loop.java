public class Loop {
    public static void main(String[] args) {
        //if loop
        int a=10;
        if (a == 10){
            System.out.println("Hello world");
        }

        //while loop
        int count=1;
        while(count!=5){
            System.out.println(count);
            count++;
        }


        //for loop
        for(int c=1;c!=5;c++){
            System.out.println("Hello world");
        }

    }
}
